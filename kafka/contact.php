<h2>Mailing Lists</h3>
	
<p>
We have a few mailing lists hosted by Apache:
</p>
<ul>
	<li>
	  <a href="mailto: kafka-users@incubator.apache.org">kafka-users@incubator.apache.org</a>: A list for general user questions about Kafka. To subscribe, send an email to <a href="mailto: kafka-users-subscribe@incubator.apache.org">kafka-users-subscribe@incubator.apache.org</a>. Archives available <a href="http://mail-archives.apache.org/mod_mbox/incubator-kafka-users">here</a>.
