	</li>
	<li>
	  <a href="mailto: kafka-commits@incubator.apache.org">kafka-commits@incubator.apache.org</a>: A list to track Kafka commits. To subscribe, send an email to <a href="mailto: kafka-commits-subscribe@incubator.apache.org">kafka-commits-subscribe@incubator.apache.org</a>. Archives available <a href="http://mail-archives.apache.org/mod_mbox/incubator-kafka-commits">here</a>.
